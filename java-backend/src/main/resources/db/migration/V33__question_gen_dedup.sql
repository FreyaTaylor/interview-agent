-- V33: 出题"高度重复"治理
-- 现象：learn/question-gen 一次生成的 {count} 道题里，多道题语义高度相似
--   （换皮重复，如"如何触发 RDB" + "RDB 在什么条件下触发" + "RDB 的触发方式有哪些"）。
-- 根因：原 prompt 只笼统要求"覆盖不同考察角度、由浅入深"，没有强制"先锁互斥维度、每维度一题"，
--   低温(0.4) 模型倾向围绕同一个最显眼的点反复换问法；avoid_section 只在 regenerate 传入，
--   且仅作"已存在题目"提示，挡不住同一批内部的换皮重复。
-- 修复：改为"先规划互斥考察维度清单，每个维度只出一题"，并把去重升级为语义级
--   （换皮、同义、改问法都算重复），与已有题目（avoid_section）也不得语义撞车。

