## ⚠️ 领域约束
**必须严格按「所属分类路径」确定题目领域！**
- 路径以 redis 开头 → 题目仅限 Redis 相关，不要错为 JS / Java / Python 同名概念
- 路径以 mysql 开头 → 题目仅限 MySQL 相关
- 即使知识点名称和其他技术同名（如"Set""线程池"），也必须出当前领域的题
- 例：redis → 数据结构 → Set → 出 Redis Set (SADD/SUNION/intset 等)，不是 JS Set

## ❗❗❗ 出题流程（必须按此顺序在心里执行，再输出）
**第 1 步：先列"考察维度"清单，再出题。不要直接写题。**
每道题必须对应一个**互不重叠的考察维度**，一个维度**最多一题**。可选维度（按需挑选，不必全用）：
- 定义辨析（是什么 / 和近义概念的区别）
- 底层原理（怎么实现 / 数据结构 / 算法）
- 触发与流程（何时发生 / 执行步骤）
- 配置与参数（关键配置、命令、默认值、调优）
- 使用场景（适用 / 不适用）
- 常见坑与排查（典型问题、报错、踩坑）
- 性能与对比（和同类方案的取舍）

**第 2 步：自检维度是否互斥（去重，重要！）。**
- 两两比较你选的维度：若两道题"考的是同一件事、只是换了问法"→ 它们是同维度，**必须合并成一题**。
- ❌ 反例（换皮重复，必须合并成一题）：
  - "如何触发 RDB？" + "RDB 在什么条件下触发？" + "RDB 的触发方式有哪些？" ← 同一维度，只留一题
  - "synchronized 的原理？" + "synchronized 底层怎么实现？" ← 同一维度，只留一题
- ✅ 正例（不同维度，保留）：
  - "RDB 如何触发？"（触发与流程） + "RDB 和 AOF 怎么选？"（性能与对比） + "RDB 文件如何加载恢复？"（底层原理）
- **与下方"避免重复"列出的已存在题目，也不得语义撞车**（同样按"换问法也算重复"判定）。

**第 3 步：每道题写明它属于哪个维度（angle 字段），再写题干与评分。**
- 输出前最后扫一遍：任意两道题的 angle 不得相同；题干换掉疑问词后不得问同一件事。

## 出题要求
1. 生成 **{count} 道题**，**题数 = 你锁定的互斥维度数**，宁可少而不重复；由浅入深排列
2. 题目简洁直接（≤25 字），面试官口吻；**一题只问一个核心点**
3. 每题给出 3-5 个 Rubric 评分点，所有 score 之和=100
4. **题干-评分一致性（必须严格）**：
    - rubric 只能覆盖该题题干明确问到的范围，不能扩展到同主题其他维度
    - 例：题干问"如何触发"，rubric 只能是触发相关（配置、命令、自动条件），不能出现"文件结构/写入流程"
    - 不要出"大而全"题
5. 每题给出 `recommended_answer`：**3-5 条要点的字符串数组**
   - 第一人称、直接陈述
   - 每条 30-80 字，包含关键概念/原理/数据/对比

