## ❗❗ 领域约束
**必须严格按「所属分类路径」确定知识点的技术领域！**
- 路径以 mysql 开头 → 讲 MySQL 相关内容，不要讲 Java
- 路径以 redis 开头 → 讲 Redis 相关内容
- 路径以 Java 开头 → 讲 Java 相关内容
- 即使知识点名称和其他领域有同名概念，也必须讲当前路径对应技术的版本
- 例：mysql → 连接数与线程池 → 讲 MySQL 的线程池（thread_pool 插件、连接管理），不是 Java ThreadPoolExecutor

## ❗❗❗ 生成流程（必须按此顺序在心里执行，再输出）
**第 1 步：先列"面试切入维度"清单，再写卡片。不要直接写卡片。**
每张卡片必须对应一个**互不重叠的面试维度**，一个维度**最多一条**。可选维度（按需挑选，不必全用）：
- 定义辨析（是什么 / 和近义概念的区别）
- 底层原理（怎么实现的 / 数据结构 / 算法）
- 使用场景（什么时候用 / 适用与不适用）
- 配置与参数（关键参数、默认值、调优）
- 常见坑与排查（典型 Bug、报错、踩坑）
- 性能与对比（和同类方案的取舍）
- 源码 / 细节（关键方法、关键字段）
- 演进历史（版本差异、变化）

**第 2 步：自检维度是否互斥。**
- 把你挑的维度两两比较：若两条卡片"面试官追问时问的是同一件事"→ 它们是同维度，**必须合并成一条**。
- ❌ 反例（同维度换皮，必须合并）：
  - 「线程池核心参数」+「线程池参数详解」+「如何设置线程池参数」← 都是"配置与参数"，只留一条
  - 「synchronized 原理」+「synchronized 底层实现」← 都是"底层原理"，只留一条
  - 「Error 与 Exception 的典型子类」+「Error 与 Exception 的典型代表」← 都是"典型代表"，只留一条
- ✅ 正例（不同维度，保留）：
  - 「synchronized 原理」(底层原理) +「synchronized vs ReentrantLock」(性能与对比) +「synchronized 的常见误用」(常见坑)

**第 3 步：每条卡片写明它属于哪个维度（angle 字段），再写 title / body。**
- 输出前最后扫一遍：任意两条 angle 不得相同；title 去掉"详解/深入/全面/如何/介绍"等虚词后不得撞车。

