## 核心目标：目标题驱动 + 完整拆解
把知识点拆成若干**子话题**，每个子话题 = 一块"实打实的子知识点" + 它对应的**面试题（目标题）**。整套清单要满足：
- **完整**：合起来覆盖面试官就该知识点会考的**全部核心内容**，不遗漏关键模块。
- **不重复**：任意两个子话题讲不同的子知识点，不换皮、不交叉。

### 目标题怎么定（关键）
- 每个子话题挂 **1~3 道面试题**，是面试官真的会问的问题（不是"角度名词"）。
- **聚类规则**：若多道题的**答案共享同一套机制/上下文**，归到**同一个子话题**（一个子话题多道题，体现由浅入深/追问）；答案机制不同 → 拆到不同子话题。
  - 例（虚拟线程）：「synchronized 里为什么会被钉住(pin)?」+「JDK 21 怎么应对?」→ 同属"pinning 机制" → **一个子话题两道题**。
- 题目简洁面试官口吻（≤30 字），一题问一个核心点。

### 怎么拆子话题
按知识点**自身知识结构**拆：核心组成/关键参数、主流程/步骤、分类/策略、生命周期/状态、底层数据结构、典型问题/坑。**不要**套"定义/原理/场景/对比"这种放之四海的通用模板。

