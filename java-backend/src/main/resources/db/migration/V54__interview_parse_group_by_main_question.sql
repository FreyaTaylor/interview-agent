-- V54: interview/parse 聚类粒度改为「按主问拆分」（一个 group = 一个主问 + 它的追问）
-- 背景：设计 2026-07-12。原 3.1 规则强制"同一技术体系连续追问合并为一个分组"（topic 级），
--        导致一个话题下多个独立主问被塞进一个 group，落库时只取 questions[0] 会丢题。
-- 现象：如 HashMap「底层结构/为什么红黑树/扩容机制」被合成一组「HashMap 原理」。
-- 修复：改为按主问粒度——主问(新探问点,可独立成题)各自开组；追问(延续上一答缺口)并入其主问组、排主问之后。
--        跨技术域必拆(3.0)不变；项目深挖仍按主题归 project 组。
-- 手段：对 3.1 的关键行做单行定点 replace（单行匹配最稳，避免重贴整段 190 行）。

UPDATE prompt_template SET content =
  replace(
  replace(
  replace(
  replace(
  replace(content,
    '### 第三步：聚类规则（极重要，宁可多合并，不要拆散）',
    '### 第三步：聚类规则（极重要：跨技术域必拆、按主问拆、真追问并入主问）'),
    '- **同一技术体系的连续追问必须合并为一个分组**，即使表面看像不同问题：',
    '- **按「主问」拆分：一个 group = 一个主问 + 它的追问**（不要把整个技术域塞进一个组）：'),
    '  - Kafka 顺序消费 → Kafka 高性能原因 → 零拷贝原理 → sendfile vs mmap = 全部归为「Kafka 高性能与零拷贝」',
    '  - **主问** = 面试官开的新探问点、能独立成题（如 HashMap「底层结构」「为什么用红黑树」「扩容机制」是三个主问）→ 各自开新组'),
    '  - HashMap 底层结构 → 为什么用红黑树 → 扩容机制 = 全部归为「HashMap 原理」',
    '  - **追问** = 延续候选人上一答的缺口/细节、脱离上文不成立（"那…呢""你刚说X，X在Y下如何""具体怎么实现""阈值多少"）→ 并入它延续的主问组、排主问之后，不单独成组'),
    '  - 判断标准：**如果追问是由前一个回答引出的，或属于同一技术主题的不同方面，就必须合并**',
    '  - 判据：这个问题是不是**因为上一答没答透才被追问出来**？是→追问并入；不是、是可独立成题的新点→主问开新组')
WHERE key = 'interview/parse';
