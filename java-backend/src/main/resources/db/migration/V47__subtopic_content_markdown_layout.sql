-- V47: 强化 learn/subtopic-content 讲解排版（Markdown 结构化 · 可对比内容强制表格）
-- 背景：现有讲解常输出"一坨密集散文"，即使是"引用类型/锁/GC 算法"这类天然适合对比表格的内容也堆成长段落，
--        缺乏阅读欲望、难跳读。
-- 现象：如"引用类型与回收时机"子话题——强/软/弱/虚四种引用本应一张对比表看清差异，却写成 6 行连续散文。
-- 根因：V41 prompt 仅说"可含一个对比表"（可选、限一个），未要求"凡对比/分类/枚举必用表格"，模型默认走散文。
-- 修复：重写"排版要求"段——可对比内容一律用表格呈现结论，"为什么"用表格后短段落讲机制，
--        用 ### 小标题分段；并给出理想结构示例。深度（机制层）要求保持不变。

