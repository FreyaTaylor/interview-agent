## ❗ 深度要求（核心）
不要罗列结论、不要"是什么"清单。必须讲到**机制层**，让读者能**推理**而非**背诵**：
- 讲**为什么**：底层机制、因果链（A 导致 B 导致 C），而不是只给结论。
- 每个关键结论都要能追溯到机制。例：不要只说"synchronized 会钉住虚拟线程"，要讲清"synchronized 依赖对象头 monitor + 绑定平台线程栈 → 虚拟线程无法 unmount → 钉住"。
- **精炼**：宁可少讲两点、每点讲透到机制，也不要面面俱到地浅。

## ❗ 排版要求（让人有读下去的欲望）
善用 Markdown 结构，**绝不要写成一坨密集散文**：
- **凡"多个东西的对比/分类/枚举"，一律用表格**呈现（如：多种引用类型、多种锁、多个 GC 算法/收集器、多个事务隔离级别、多种集合实现）。表格列出对比维度，一眼看清差异。
- 表格只放"可对比的结论"；**"为什么"用表格后的短段落讲机制**——表格给结论、段落给因果，两者配合。
- 用 `###` 小标题分段（如"回收时机""应用场景""底层机制"）；关键词 **加粗**；可含 ≤12 行代码块。
- 每段聚焦一点，每句 ≤30 汉字，能跳读。
- **不要**写"总结/小结"段；**不要**在正文里嵌面试题问句（目标题已单独展示）。

## 理想结构示例（仅示意排版骨架，勿照抄内容/主题）
> 若子话题是"引用类型与回收时机"：先一句点题 → 一个四行对比表（引用类型 × 回收时机 × get()能否拿到 × 典型用途）→ 两段机制讲解（"为什么软引用能卡到 OOM 前""为什么弱引用逢 GC 必死"）→ 一个 `### 应用场景` 小节用要点列出。

