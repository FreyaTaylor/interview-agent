-- V51: interview/parse —— 过滤"非技术的团队/流程/管理/闲聊类"无意义问题
-- 背景：解析出的问题里混入"你们组几个人？""代码有人 Code Review 吗？""这个方案没人挑战过吗？"这类
--        非技术、无实质知识点的软性/流程/管理问题，污染知识点/项目复盘。
-- 现象：V23 第一步"忽略非提问内容"已排除寒暄/过渡/反问/纯摸底/反馈，但未明确排除团队规模/流程/管理类。
-- 修复：在忽略清单追加一条，明确跳过这类问题；同时强调真正的技术追问（走索引/统计信息更新等）必须保留。
-- 用 replace() 定点插入，避免重贴 190 行整段 prompt。

UPDATE prompt_template
SET content = replace(
    content,
    '- **面试官给出的建议/反馈**：面试官点评候选人表现、给建议 — 不是提问，跳过',
    '- **面试官给出的建议/反馈**：面试官点评候选人表现、给建议 — 不是提问，跳过
- **非技术的团队/流程/管理/闲聊类问题**：如"你们这个组有几个人？""你一般代码有人 Code Review 吗？""这个方案/方法没人挑战/质疑过吗？"、汇报关系、开发流程、加班情况、公司规模等 — 无实质技术知识点，直接跳过，不生成分组。
  ⚠️ 注意区分：真正的**技术追问**必须保留，例如"统计信息是更新某个列吗？""不可以让它走索引吗？""统计信息频繁更新会不会有影响？""既然确定能走索引，为什么不强制指定索引？"——这些有技术内容，属于 knowledge/project，不能当作无意义问题跳过。'
)
WHERE key = 'interview/parse';
