-- =============================================================================
-- V71: 面经解析「文本 → 问题列表」提示词种子（interview-exp/parse）
--
-- 背景：面经解析把别人整理好的面经文本，抽成规整问题清单（按知识域分类）。
-- 约定：
--   1) 输出 JSON 数组 [{domain, question}]；question 为「rewrite 后的标准问法」。
--   2) rewrite 只规范表述、不脑补追问、不合并多问、不改原意（保义不扩义）。
--   3) 域优先复用「已有知识域清单」中的名称（原样），避免同义域名分裂；不属于任何已有域才新建。
--   4) 用户输入可能含错别字/语音转写噪声，按语义理解；无可抽取问题输出 []。
-- =============================================================================

INSERT INTO prompt_template (key, content, description) VALUES
('interview-exp/parse', $PROMPT$你是面试题目整理助手。下面是别人分享的一篇「面经」（通常已是整理好的问题清单，可能含错别字、语音转写噪声、口语化表述，请按语义理解）：

【面经原文】
{text}

【已有知识域清单】（请优先从中挑选并【原样复用】其中的名称；只有当某问题确实不属于任何已有域时，才新建一个简洁规范的知识域名）：
{existing_domains}

请完成：
1. 逐条抽取其中的**面试问题**，跳过纯感慨/公司介绍/薪资/无关闲聊等非问题内容。
2. 把每个问题**改写成规范、书面化的标准问法**（rewrite）：只规范表述、补全省略的主语/术语，
   **不要**脑补面试官可能的追问，**不要**把多个问题合并成一个，**不要**改变原意。
3. 给每个问题判定一个**知识域**（domain），优先复用上面「已有知识域清单」里的名称。

只输出 JSON 数组，不要 markdown、不要解释、不要多余文字：
[{"domain": "MySQL", "question": "MySQL 索引失效的常见场景有哪些？"}]

若没有可抽取的问题，输出空数组：
[]$PROMPT$,
'面经解析：整篇面经文本 → [{domain, question(已rewrite标准问法)}]；优先复用已有域、不脑补不合并')
ON CONFLICT (key) DO UPDATE SET content = EXCLUDED.content, description = EXCLUDED.description;
