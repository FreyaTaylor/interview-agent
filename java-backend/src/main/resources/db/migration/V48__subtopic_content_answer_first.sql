-- V48: learn/subtopic-content 讲解 prompt 改为"答案先于讲解"（学考同源）
-- 背景：设计 2026-07-11-learn-answer-first-rubric.md。讲解生成前先为目标题产采分点(rubric)，
--        讲解应服从采分点契约——必须逐点讲清，而非事后自检"能不能答"。
-- 现象：旧 prompt 只有 {target_questions}（题干），靠尾部自检隐式对齐，讲解可能发散/漏点/滚雪球。
-- 根因：答案是讲解的副产物，方向反了。
-- 修复：新增模板变量 {answer_points}（每题的必讲采分点），把自检升级为"必须逐点讲清采分点"。
--        采分点由 study/rubric-gen 先生成、只覆盖题目字面 → 讲解范围随之收敛。

INSERT INTO prompt_template (key, content, description) VALUES
('learn/subtopic-content', $PROMPT$你是一位资深技术面试辅导专家。请为下面这个子话题写一段**深度讲解正文**，目标是：读完能**自如回答**列出的面试题、并**覆盖每个采分点**。**只返回 Markdown 正文**（不要 JSON、不要标题行、不要围栏）。

## 子话题
{title}

## 所属分类路径
{category_path}

## 学完应能回答的面试题（目标题）
{target_questions}

## ❗ 必须讲清的采分点（讲解的硬契约）
下面是每道目标题的**评分采分点**（已按题目字面圈定范围）。你的讲解**必须逐点讲透每一个采分点**——这是评分依据，漏讲=学员答不出=扣分：
{answer_points}

## ❗ 深度要求（核心）
不要罗列结论、不要"是什么"清单。必须讲到**机制层**，让读者能**推理**而非**背诵**：
- 讲**为什么**：底层机制、因果链（A 导致 B 导致 C），而不是只给结论。
- 每个采分点都要追溯到机制。例：不要只说"synchronized 会钉住虚拟线程"，要讲清"synchronized 依赖对象头 monitor + 绑定平台线程栈 → 虚拟线程无法 unmount → 钉住"。
- **只讲采分点覆盖的范围**：关联但题目没问的知识点不展开（那是别的题），避免滚雪球。

## ❗ 排版要求（让人有读下去的欲望）
善用 Markdown 结构，**绝不要写成一坨密集散文**：
- **凡"多个东西的对比/分类/枚举"，一律用表格**呈现（如：多种引用类型、多种锁、多个 GC 算法/收集器、多个事务隔离级别）。表格列出对比维度，一眼看清差异。
- 表格只放"可对比的结论"；**"为什么"用表格后的短段落讲机制**——表格给结论、段落给因果。
- 用 `###` 小标题分段；关键词 **加粗**；可含 ≤12 行代码块。
- 每段聚焦一点，每句 ≤30 汉字，能跳读。
- **不要**写"总结/小结"段；**不要**在正文里嵌面试题问句（目标题已单独展示）。

## 自检（输出前必做）
1. 逐条看**采分点**：这段正文有没有**把每个采分点讲到机制层**？漏的补上。
2. 有"对比/分类/枚举"却写成散文了吗？→ 改成表格。
3. 有没有讲了采分点**之外**的发散内容？→ 删掉，留给别的题。

只返回 Markdown 正文。$PROMPT$, 'Learn 模块：单子话题深度正文（答案先·讲解服从采分点契约 + Markdown 结构化排版）')
ON CONFLICT (key) DO UPDATE SET content = EXCLUDED.content, description = EXCLUDED.description;
