('interview-exp/question-content', $PROMPT$你是一位资深技术面试辅导专家。请为下面这道**面经里的面试题**写一段**深度讲解正文**，目标是：读完能**自如回答**这道题、并**覆盖每个采分点**。**只返回 Markdown 正文**（不要 JSON、不要标题行、不要围栏）。

## 面试题
{question}

## 所属知识域
{domain}

## ❗ 必须讲清的采分点（讲解的硬契约）
下面是这道题的**评分采分点**（已按题目字面圈定范围）。你的讲解**必须逐点讲透每一个采分点**——这是评分依据，漏讲=答不出=扣分：
{answer_points}

## ❗ 深度要求（核心）
不要罗列结论、不要"是什么"清单。必须讲到**机制层**，让读者能**推理**而非**背诵**：
- 讲**为什么**：底层机制、因果链（A 导致 B 导致 C），而不是只给结论。
- 每个采分点都要追溯到机制。
- **只讲采分点覆盖的范围**：关联但题目没问的知识点不展开，避免滚雪球。

## ❗ 排版要求（让人有读下去的欲望）
善用 Markdown 结构，**绝不要写成一坨密集散文**：
- **凡"多个东西的对比/分类/枚举"，一律用表格**呈现，表格列出对比维度，一眼看清差异。
- 表格只放"可对比的结论"；**"为什么"用表格后的短段落讲机制**。
- 用 `###` 小标题分段；关键词 **加粗**；可含 ≤12 行代码块。
- 每段聚焦一点，每句 ≤30 汉字，能跳读。
- **不要**写"总结/小结"段；**不要**在正文里嵌面试题问句。

## 自检（输出前必做）
1. 逐条看**采分点**：这段正文有没有**把每个采分点讲到机制层**？漏的补上。
2. 有"对比/分类/枚举"却写成散文了吗？→ 改成表格。
3. 有没有讲了采分点**之外**的发散内容？→ 删掉。

只返回 Markdown 正文。$PROMPT$, '看看面经：单问题深度讲解正文（答案先·讲解服从采分点契约 + Markdown 结构化排版）')
