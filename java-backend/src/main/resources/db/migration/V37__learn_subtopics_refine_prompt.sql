-- V37: 子话题"两步生成"的第二步 —— 审校去重 + 补全 prompt
-- 背景：单次 LLM 生成即便 prompt 里约束了"完整 + 不重复"，仍会出现语义重复的子话题
--   （如「虚拟线程的定义与核心目标」vs「虚拟线程的定义与设计目标」），且代码层无去重兜底。
-- 方案（两步 workflow，可用 iagent.learn.subtopics.strategy=two-step 开启，默认 single 走旧逻辑）：
--   第一步：learn/subtopics-gen 照常产候选列表；
--   第二步：learn/subtopics-refine 拿到候选列表做"合并语义重复 + 补齐遗漏 + 输出最终清单"。
-- 用 LLM 审校（拿到完整上下文对比）判重，比 embedding 阈值判短标题更准。

INSERT INTO prompt_template (key, content, description) VALUES
('learn/subtopics-refine', $PROMPT$你是一位资深技术面试辅导专家。下面是针对某个知识点**第一步生成的候选子话题清单**，可能存在**语义重复**或**关键模块遗漏**。请你审校后输出**最终清单**，**必须只返回 JSON 数组**。

## 知识点
{knowledge_point}

## 所属分类路径
{category_path}

## 第一步生成的候选清单（JSON）
{subtopics_json}

## ❗❗ 领域约束
**必须严格按「所属分类路径」确定技术领域**（mysql→MySQL、redis→Redis、Java→Java），即使概念在别的领域有同名，也讲当前路径对应技术的版本。

## 你的任务（审校三件事）
1. **合并语义重复**：逐条对比，任意两条若讲的是**同一个子知识点**（只是换了说法 / 换了角度 / title 近似），**合并成一条**，保留更准确的 title，正文取两者更完整的版本。
   - 例：「定义与核心目标」和「定义与设计目标」是同一块内容 → 合并。
2. **补齐遗漏**：通盘看整套清单，若面试常考的**关键模块缺失**，补上新的子话题。
3. **保持完整不重复**：最终清单合起来应覆盖面试会考的全部核心内容，且任意两条互不交叉。

## 硬规则（不许违反）
- **完全相同或高度相似的 title 绝对不允许出现两次。**
- title 必须聚焦**单一子知识点**，禁止用"与/和/及/、"把两个维度塞进一个 title。
- body_md 里**不要**嵌任何"面试常问 / 面试追问"问题行（任何标记都不放）。
- **不要**生成"总结"/"小结"子话题。

## 输出 schema（严格，与第一步一致）
```json
[
  {
    "title": "一个具体的子知识点（≤25 字，全列表唯一）",
    "body_md": "Markdown 正文，2-4 段简洁专业，关键词 **加粗**；可含 ≤10 行代码块或对比表",
    "importance": 4
  }
]
```

## 字段要求
- `title`：必填，非空，不含 `#`，**全列表唯一**，是具体子知识点
- `body_md`：必填，纯 Markdown，不要再嵌 `####` 子标题
- `importance`：整数 1-5（5=必考、4=高频、3=常考、2=偶现、1=冷门）

## 数量与顺序
- 条数 = 覆盖完整所需，通常 5-10 条，**不凑数、不遗漏**。
- 按"由浅入深 / 面试常考顺序"排列。

只返回 JSON 数组，不要 Markdown 围栏、不要解释文字。$PROMPT$, 'Learn 模块：子话题两步生成的第二步——审校去重+补全（合并语义重复、补齐遗漏、输出最终清单）')
ON CONFLICT (key) DO UPDATE SET content = EXCLUDED.content, description = EXCLUDED.description;
