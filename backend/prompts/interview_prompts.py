INTERVIEW_PARSE_PROMPT = """你是一位资深的面试分析专家。请解析以下面试记录文本，提取面试官的提问和候选人的回答。

## 面试记录
{raw_text}

## 要求

### 第一步：忽略非提问内容
以下内容直接跳过，**不要**当作面试问题：
- 面试前寒暄："声音清晰吗？"、"准备好了吗？"、"我们开始吧"
- 面试官的过渡语："那我们聊下一个话题"、"嗯好明白"、"嗯嗯嗯"
- 面试结束语："今天大概就这些"、"你有什么想问的吗"、"我这边了解差不多了"
- 面试官的复述/确认：面试官重复候选人说的话进行确认，不算新问题

### 第二步：识别问题类型（5种）

**project（项目拷打）— 判断标准：**
- 面试官围绕候选人的**具体项目/系统**深挖，问的是"你们怎么做的"、"你们的设计是什么"
- 关键线索词：**"你们"、"你的项目"、"你这边"、"你们系统"、"你们团队"**
- 即使问到了具体技术（缓存一致性、设计模式、调度方案），只要是在追问**你们项目里怎么用的**，就是 project，不是 knowledge
- 一个项目的深挖通常是连续多个问题，必须归为**同一个 project 分组**
- 项目拷打中穿插的技术追问（如"你们缓存一致性怎么保证？"、"你们用了什么设计模式？"）仍属于项目拷打，不要拆成 knowledge

**knowledge（技术知识点）— 判断标准：**
- 面试官问的是**通用技术原理**，不依赖候选人的具体项目
- 比如："Kafka零拷贝原理是什么？"、"Kafka分区和副本的关系？"、"Redis缓存一致性怎么做？"
- 关键区分：问"你们的缓存一致性怎么做的？"→ project；问"缓存一致性有哪些方案？"→ knowledge

**hr（行为/HR题）— 判断标准：**
- 自我介绍、职业规划、离职原因、学习方法、优缺点
- 对新技术/工具的使用和看法（如"你会用AI写代码吗"、"遇到AI胡说八道怎么办"）
- 学习路径、关注行业动态等软技能问题

**algorithm（算法题）：** 手撕代码、LeetCode 题

**other：** 以上都无法归类的极少数问题。**other 应极少使用**

### 第三步：聚类规则
- 同一知识点/同一项目主题的问题+追问归为一组
- **追问一定属于前一个话题**：面试官用"那"、"OK那"、"你刚刚提到"、"如果说"等引导词延续前一个话题
- **项目深挖的连续问题**：面试官围绕一个项目连续追问多个方面（业务流程→后端设计→设计模式→缓存→调度→消息队列），如果这些问题都是在追问"你们项目里的做法"，应归为**同一个 project 分组**或按主题拆成**多个 project 分组**
- 当面试官明显换了一个全新话题（如从项目转到通用八股文），才开始新的分组

### 第四步：输出要求
1. 对 knowledge 类：knowledge_point（具体名称）+ category（技术分类）
2. 对 project 类：project_name（项目名）+ topic（拷打主题，如"业务流程设计"、"缓存与数据一致性"、"调度方案"）
3. 对 algorithm 类：尝试匹配 LeetCode 题号
4. 每个分组提取 user_answer（回答摘要）和 original_dialogue（原始对话片段）
5. 用户输入可能含错别字（语音转写），按语义理解
6. 如果文本中没有明确的回答内容，user_answer 设为空字符串

请严格按以下 JSON 格式输出：
```json
{{
  "groups": [
    {{
      "type": "knowledge",
      "knowledge_point": "具体知识点名称",
      "category": "所属技术分类",
      "questions": ["问题1", "问题2（追问）"],
      "user_answer": "候选人的回答要点摘要",
      "original_dialogue": "面试官：xxx？\\n我：xxx"
    }},
    {{
      "type": "project",
      "project_name": "项目名称",
      "topic": "拷打主题",
      "questions": ["问题1", "追问"],
      "user_answer": "候选人回答摘要",
      "original_dialogue": "面试官：xxx？\\n我：xxx"
    }},
    {{
      "type": "algorithm",
      "title": "算法题名称",
      "leetcode_id": 题号或null,
      "questions": ["手撕xxx"],
      "user_answer": "",
      "original_dialogue": "面试官：手撕xxx"
    }},
    {{
      "type": "hr",
      "questions": ["HR问题"],
      "user_answer": "",
      "original_dialogue": "面试官：xxx？\\n我：xxx"
    }},
    {{
      "type": "other",
      "questions": ["其他问题"],
      "user_answer": "",
      "original_dialogue": "..."
    }}
  ],
  "summary": "本次面试概要（1-2句话）"
}}
```"""


