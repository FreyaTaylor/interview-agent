"""
知识树初始化 Prompt 模板
- 骨架生成（一级+二级分类）
- 叶子展开（三级知识点+权重）
"""

# Step 1: 生成一级+二级骨架
TREE_SKELETON_PROMPT = """你是一位资深 Java 后端面试辅导专家。请根据用户画像，生成一棵面试知识树的**一级分类 + 二级分类**骨架。

## 用户画像
{profile_text}

## 要求
1. 一级分类覆盖 Java 后端面试的核心领域（通常 8-15 个），根据用户画像调整侧重
2. 每个一级分类下生成 2-6 个二级分类
3. 用户提到的薄弱方向，对应的分类要**更细**（多拆几个二级）
4. 用户提到的项目经验，生成「项目经验」一级分类，下设具体项目名称作为二级
5. 不需要生成三级叶子节点（下一步单独展开）
6. 排序：高频考点靠前

## 输出格式
```json
{{
  "categories": [
    {{
      "name": "一级分类名称",
      "sort_order": 1,
      "children": [
        {{"name": "二级分类名称", "sort_order": 1}},
        {{"name": "二级分类名称", "sort_order": 2}}
      ]
    }}
  ]
}}
```

只返回 JSON，不要其他内容。"""


# Step 2: 展开二级分类为三级叶子知识点
TREE_EXPAND_PROMPT = """你是一位资深 Java 后端面试辅导专家。请将以下二级分类展开为具体的**三级叶子知识点**（面试最小考察单元）。

## 用户画像
{profile_text}

## 一级分类
{category_name}

## 要排展开的二级分类
{subcategory_name}

## 要求
1. 每个叶子知识点是面试中可以独立提问的最小单元
2. 每个知识点标注面试权重 ★1-5（5=高频必考，1=低频了解即可）
3. 根据用户画像调整权重：用户薄弱/目标方向的权重适当提高
4. 通常 3-8 个叶子节点，不要太粗也不要太细
5. 如果是项目经验类，叶子节点是具体的技术方案/设计决策（如"超时取消方案"、"库存扣减方案"）

## 输出格式
```json
{{
  "leaves": [
    {{"name": "知识点名称", "interview_weight": 5, "sort_order": 1}},
    {{"name": "知识点名称", "interview_weight": 3, "sort_order": 2}}
  ]
}}
```

只返回 JSON，不要其他内容。"""
