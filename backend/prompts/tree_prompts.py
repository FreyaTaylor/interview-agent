# Step 1: 生成一级+二级骨架
TREE_SKELETON_PROMPT = """你是一位资深 Java 后端面试辅导专家。请根据用户画像，生成一棵面试知识树的**一级分类 + 二级分类**骨架。

## 用户画像
{profile_text}

## 要求
1. 一级分类 **8-12 个**，覆盖 Java 后端面试核心领域，根据用户画像调整侧重
2. 每个一级分类下的二级分类数量**根据该领域的知识丰富度自行决定**（MySQL/Redis 等内容多的可以 4-6 个，计算机网络等偏少的 2-3 个即可）
3. **二级分类名称简短**（≤8字），不要在名称中列举具体技术（❌ "JUC核心工具（AQS、ReentrantLock）" → ✅ "并发工具类"）
4. **不要太偏门**：容器化/云原生/服务网格等除非用户画像明确提到，否则不生成
5. **不生成「项目经验」分类**，项目相关内容从面试复盘模块获取
6. 不需要生成三级叶子节点（下一步单独展开）
7. 排序：高频考点靠前
8. **设计模式**如果作为一级分类，二级只保留高频的（创建型、结构型、行为型各取 1-2 个高频），不要全列

### 正确示例 ✅
一级"Java并发" → 二级: 线程基础 | 锁机制 | 并发工具类 | 线程池 | 并发容器
一级"MySQL" → 二级: 索引原理 | 事务与锁 | SQL调优 | 日志机制 | 分库分表 | 主从与高可用（6 个，因为 MySQL 内容多）
一级"计算机网络" → 二级: TCP/IP | HTTP（2 个，因为内容相对少）

### 错误示例 ❌
一级"Java并发编程" → 二级: "JUC核心工具（AQS、ReentrantLock、CountDownLatch、CyclicBarrier、Semaphore）"（名称太长，把叶子塞进二级了）
每个一级都生成 4 个二级（太均匀，MySQL 和计网不应该一样多）

## 输出格式
```json
{{
  "categories": [
    {{
      "name": "一级分类名称",
      "sort_order": 1,
      "children": [
        {{"name": "二级分类名称", "sort_order": 1}},
        {{"name": "二级分类名称", "sort_order": 2}}
      ]
    }}
  ]
}}
```

只返回 JSON，不要其他内容。"""


# Step 2: 展开分类为完整子树（一次返回，LLM自行决定层级深度）
TREE_EXPAND_PROMPT = """你是一位资深 Java 后端面试辅导专家。请将以下分类展开为完整的子树结构。

## 用户画像
{profile_text}

## 当前路径
{node_path}

## 已有的知识点（全局去重）
{existing_leaves}

## 核心规则

**自行决定每个节点是"子分类"还是"叶子知识点"：**

- **子分类**（有 children）：该概念下还有多个独立的子领域，需要再分一层
  - 例："锁机制" → children: [synchronized, ReentrantLock, 乐观锁与CAS]
  - 例："索引" → children: [B+树索引, 联合索引, 索引优化]

- **叶子知识点**（无 children）：已经足够具体，可以直接出面试题
  - 例："synchronized" 是叶子（面试官直接问锁升级、偏向锁等）
  - 例："B+树索引" 是叶子（面试官直接问结构、优劣、和B树区别）

**层级不固定**，由内容丰富度决定：
- 简单的分类可能只有 1 层叶子（如"HTTP" → 叶子：状态码、缓存机制、HTTPS）
- 复杂的分类可能有 2-3 层（如"并发" → "锁机制" → "synchronized" → 叶子）
- **最深不超过 3 层**（二级分类下最多再分 3 层）

## 要求
1. 名称简短（≤10字），具体技术概念
2. 每个节点标注面试权重 1-5
3. 数量根据内容丰富度决定，不为凑数放冷门
4. 不要和已有知识点重复
5. 只放大厂面试高频内容
6. 冷门不放：建造者/访问者等冷门设计模式、Exchanger/Phaser等、inode/目录项等
7. 太泛不放：❌"缓存" → ✅"缓存穿透"

## 输出格式（嵌套结构）
```json
{{
  "children": [
    {{
      "name": "子分类名称",
      "interview_weight": 5,
      "children": [
        {{"name": "叶子知识点", "interview_weight": 5}},
        {{"name": "叶子知识点", "interview_weight": 4}}
      ]
    }},
    {{
      "name": "直接叶子", 
      "interview_weight": 4
    }}
  ]
}}
```

有 children 字段 = 子分类，没有 children 字段 = 叶子知识点。只返回 JSON。"""


