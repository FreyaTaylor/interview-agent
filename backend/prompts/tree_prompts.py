# Step 1: 生成一级+二级骨架
TREE_SKELETON_PROMPT = """你是一位资深 Java 后端面试辅导专家。请根据用户画像，生成一棵面试知识树的**一级分类 + 二级分类**骨架。

## 用户画像
{profile_text}

## 要求
1. 一级分类 **8-12 个**，覆盖 Java 后端面试核心领域，根据用户画像调整侧重
2. 每个一级分类下的二级分类数量**根据该领域的知识丰富度自行决定**（MySQL/Redis 等内容多的可以 4-6 个，计算机网络等偏少的 2-3 个即可）
3. **二级分类名称简短**（≤8字），不要在名称中列举具体技术（❌ "JUC核心工具（AQS、ReentrantLock）" → ✅ "并发工具类"）
4. **不要太偏门**：容器化/云原生/服务网格等除非用户画像明确提到，否则不生成
5. **不生成「项目经验」「项目实战」类分类**，项目相关内容从面试复盘模块获取
6. 不需要生成三级叶子节点（下一步单独展开）
7. 排序：高频考点靠前
8. **设计模式**如果作为一级分类，二级只保留高频的（创建型、结构型、行为型各取 1-2 个高频），不要全列

### 正确示例 ✅
一级"Java并发" → 二级: 线程基础 | 锁机制 | 并发工具类 | 线程池 | 并发容器
一级"MySQL" → 二级: 索引原理 | 事务与锁 | SQL调优 | 日志机制 | 分库分表 | 主从与高可用（6 个，因为 MySQL 内容多）
一级"计算机网络" → 二级: TCP/IP | HTTP（2 个，因为内容相对少）

### 错误示例 ❌
一级"Java并发编程" → 二级: "JUC核心工具（AQS、ReentrantLock、CountDownLatch、CyclicBarrier、Semaphore）"（名称太长，把叶子塞进二级了）
每个一级都生成 4 个二级（太均匀，MySQL 和计网不应该一样多）

## 输出格式
```json
{{
  "categories": [
    {{
      "name": "一级分类名称",
      "sort_order": 1,
      "children": [
        {{"name": "二级分类名称", "sort_order": 1}},
        {{"name": "二级分类名称", "sort_order": 2}}
      ]
    }}
  ]
}}
```

只返回 JSON，不要其他内容。"""


# Step 2: 展开分类为面试知识方向（扁平叶子，不再生成子分类）
TREE_EXPAND_PROMPT = """你是一位资深 Java 后端面试官。请列出「{node_path}」这个方向下，**面试中会考到的具体知识方向**。

## 用户画像
{profile_text}

## 已有的考点（全局去重）
{existing_leaves}

## ⚠️ 重要约束
**只列「{node_path}」方向下的面试知识方向！不要跨方向！**

## 核心规则

**每个节点是一个"面试知识方向"**——面试官可以围绕它出 3-8 道题的知识单元。

### 粒度标准（关键！）
节点粒度 = 面试知识方向，而非单个考题。一个节点应该能包含 3-8 个具体面试问题。

✅ 正确粒度：
- "锁机制" — 包含 synchronized/ReentrantLock/锁升级/偏向锁/轻量级锁等具体面试题
- "线程池" — 包含 核心参数/工作流程/拒绝策略/线程工厂等具体面试题
- "事务与MVCC" — 包含 隔离级别/MVCC实现/ReadView/undo log等具体面试题
- "消息可靠性" — 包含 发送确认/持久化/消费确认/死信队列等具体面试题
- "消息顺序性" — 包含 全局顺序/分区顺序/顺序消费等具体面试题

❌ 粒度太细（这些应该是知识讲解中的子章节，而非独立节点）：
- "三次握手" — 这是 TCP 连接管理方向下的一个子话题
- "偏向锁" — 这是锁机制方向下的一个子话题
- "生产者-消费者模型" — 这是消息模型方向下的一个子话题
- "刷盘策略" — 这是可靠性方向下的一个子话题

❌ 粒度太粗：
- "Java并发" — 这是一级分类，不是叶子节点
- "MySQL" — 太宽泛

### 层级规则
- **所有节点必须是扁平叶子**，不允许嵌套子分类
- 通常 4-8 个叶子节点
- 每个节点的具体子话题会由知识讲解模块自动展开为子章节

## 要求
1. 名称简短（≤10字），是面试官会提到的知识方向
2. 每个节点标注面试权重 1-5
3. 数量根据实际考频决定，不凑数
4. 不要和已有考点重复
5. 只放大厂 3 年经验 Java 后端高频知识方向

## 输出格式（扁平列表）
```json
{{
  "children": [
    {{"name": "知识方向名称", "interview_weight": 5}},
    {{"name": "知识方向名称", "interview_weight": 4}},
    {{"name": "知识方向名称", "interview_weight": 3}}
  ]
}}
```

所有节点都是叶子，不允许有 children。只返回 JSON。"""


