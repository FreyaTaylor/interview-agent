"""
知识树初始化 Prompt 模板
- 骨架生成（一级+二级分类）
- 叶子展开（三级知识点+权重）
"""

# Step 1: 生成一级+二级骨架
TREE_SKELETON_PROMPT = """你是一位资深 Java 后端面试辅导专家。请根据用户画像，生成一棵面试知识树的**一级分类 + 二级分类**骨架。

## 用户画像
{profile_text}

## 要求
1. 一级分类 **8-12 个**，覆盖 Java 后端面试核心领域，根据用户画像调整侧重
2. 每个一级分类下的二级分类数量**根据该领域的知识丰富度自行决定**（MySQL/Redis 等内容多的可以 4-6 个，计算机网络等偏少的 2-3 个即可）
3. **二级分类名称简短**（≤8字），不要在名称中列举具体技术（❌ "JUC核心工具（AQS、ReentrantLock）" → ✅ "并发工具类"）
4. **不要太偏门**：容器化/云原生/服务网格等除非用户画像明确提到，否则不生成
5. **不生成「项目经验」分类**，项目相关内容从面试复盘模块获取
6. 不需要生成三级叶子节点（下一步单独展开）
7. 排序：高频考点靠前

### 正确示例 ✅
一级"Java并发" → 二级: 线程基础 | 锁机制 | 并发工具类 | 线程池 | 并发容器

### 错误示例 ❌
一级"Java并发编程" → 二级: "JUC核心工具（AQS、ReentrantLock、CountDownLatch、CyclicBarrier、Semaphore）"（名称太长，把叶子塞进二级了）

## 输出格式
```json
{{
  "categories": [
    {{
      "name": "一级分类名称",
      "sort_order": 1,
      "children": [
        {{"name": "二级分类名称", "sort_order": 1}},
        {{"name": "二级分类名称", "sort_order": 2}}
      ]
    }}
  ]
}}
```

只返回 JSON，不要其他内容。"""


# Step 2: 展开二级分类为三级叶子知识点
TREE_EXPAND_PROMPT = """你是一位资深 Java 后端面试辅导专家。请将以下二级分类展开为具体的**三级叶子知识点**。

## 用户画像
{profile_text}

## 一级分类
{category_name}

## 要展开的二级分类
{subcategory_name}

## 已有的知识点（全局去重，不要生成重复的）
{existing_leaves}

## 什么是"知识点"？

知识点 = 一个**技术概念或原理**，面试中围绕它可以出 3-5 个不同角度的问题。
知识点**不是**一道面试题，也**不是**某个具体的实现细节。

### 正确示例 ✅
- `ConcurrentHashMap` — 面试官可以从底层结构、put流程、扩容等多角度提问
- `Redis 分布式锁` — 面试官会追问 SETNX、看门狗、RedLock 等
- `G1 垃圾回收器` — 面试官会问 Region、RSet、Mixed GC 等

### 错误示例 ❌（太细，是"问题"不是"知识点"）
- `ConcurrentHashMap 的 put 操作流程`
- `volatile 禁止指令重排序的底层实现（内存屏障）`
- `Redisson分布式锁的基本原理（Watchdog自动续期机制）`

## 要求
1. 知识点名称简短（≤10字），是技术概念名词
2. 每个知识点标注面试权重 ★1-5（5=高频必考）
3. 叶子数量**根据该二级分类的知识丰富度自行决定**（内容多的可以 5-8 个，内容少的 2-3 个即可，不要为了凑数放冷门）
4. **不要和「已有的知识点」重复**（名称相同或含义相同都算重复）
5. **只放面试高频知识点**，以下冷门内容不要生成：
   - Exchanger、Phaser、StampedLock 等冷门并发工具
   - 注解处理器/APT、泛型桥接方法等编译器细节
   - 具体项目的业务逻辑（如"甘特图计算"、"滚动窗口优化"）
   - 判断标准：**大厂面试 3 年经验 Java 后端大概率会问到的才放**

## 输出格式
```json
{{
  "leaves": [
    {{"name": "知识点名称", "interview_weight": 5, "sort_order": 1}},
    {{"name": "知识点名称", "interview_weight": 3, "sort_order": 2}}
  ]
}}
```

只返回 JSON，不要其他内容。"""


