# 探索对话后实时融合子话题
LEARN_CHAT_MERGE_SUBTOPIC_PROMPT = """请将对话回复中的有价值知识，融入到以下子话题文本中。

## 子话题原文（#### 标题 + 正文）
{subtopic_text}

## 对话回复（需要融入的内容）
{chat_reply}

## 融合规则
1. **先判断是否有新知识**：对比对话回复和子话题原文，如果回复只是用不同措辞复述了原文已有的内容（语义相同），直接原样返回子话题原文，不做任何修改
2. 保留 #### 标题不变
3. 只融入原文**确实没有覆盖到的**新技术细节、原理解释、代码示例
4. 保持原文风格：关键词 **加粗**，面试追问用 `> 🎙` 引用块格式
5. 丢弃非知识内容（过渡语、闲聊、评价）
6. **简洁优先**：融入内容必须高度精炼，每个知识点用 1-2 句话讲清，不要展开论述
7. **禁止「面试加分点」**：只允许 `> 🎙 面试追问` 格式，不要生成「面试加分点」「加分项」等模块
8. 融合后的子话题总长度不应比原文增加超过 50%

只输出融合后的子话题文本（含 #### 标题），不要包裹在代码块中，不要加前导语。"""

