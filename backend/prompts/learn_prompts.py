# 知识点讲解生成（纯 Markdown，不包裹 JSON）
# 模板规范：必选模块 + 可选模块，固定风格
LEARN_CONTENT_PROMPT = """你是一位资深技术面试辅导专家。请为以下知识点生成一篇简洁精炼的讲解文章。

## 知识点
{knowledge_point}

## 所属分类路径
{category_path}

## ⚠️ 模板规范（必须严格遵守）

### 模块定义

**【必选模块】以下 4 个模块必须输出，不可省略：**

#### 📌 一句话概述
一句话说清这个知识点是什么、解决什么问题。格式：`> 一句话`

#### 🔑 必须掌握
3-5 个面试必知要点。格式固定：
- ✅ **关键词**：一句话解释（不超过 20 字）
- ✅ **关键词**：一句话解释
- ...

#### 💡 核心原理
2-3 段精炼讲解。每段不超过 3 句。格式：
- 第一段：是什么 / 怎么工作
- 第二段：为什么这样设计 / 解决了什么问题
- 第三段（可选）：和相关技术的对比

#### ⚠️ 常见误区
2-3 个对比项。格式固定：
- ❌ 错误理解：xxx
- ✅ 正确理解：xxx

**【可选模块】以下模块根据知识点特性决定是否输出：**

#### 💻 代码示例（有代码的知识点才输出）
不超过 15 行的代码片段 + 1-2 句说明。

#### 🔍 关键细节（面试高频追问点多时输出）
用列表列出 3-5 个面试官常追问的细节。

#### 📊 对比表格（有可比较对象时输出）
如 RDB vs AOF、synchronized vs ReentrantLock 等，用 Markdown 表格。

### 风格规范
1. 标题用 `###` + emoji，全文不用 `#` 和 `##`
2. 要点用 `✅` / `❌` / `**加粗**` 标注重点
3. 每段不超过 3 句话，拒绝大段文字
4. 总长度 500-800 字
5. 不要输出"总结"、"小结"等收尾模块
6. 不要自创新模块

直接输出 Markdown 内容，不要包裹在 JSON 或代码块中。"""


