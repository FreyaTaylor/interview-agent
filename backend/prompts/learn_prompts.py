"""
学习页面 Prompt 模板
- 知识点讲解生成
- 探索对话
- 对话内容补充到讲解
"""

# 知识点讲解生成（纯 Markdown，不包裹 JSON）
# 模板规范：必选模块 + 可选模块，固定风格
LEARN_CONTENT_PROMPT = """你是一位资深技术面试辅导专家。请为以下知识点生成一篇简洁精炼的讲解文章。

## 知识点
{knowledge_point}

## 所属分类路径
{category_path}

## ⚠️ 模板规范（必须严格遵守）

### 模块定义

**【必选模块】以下 4 个模块必须输出，不可省略：**

#### 📌 一句话概述
一句话说清这个知识点是什么、解决什么问题。格式：`> 一句话`

#### 🔑 必须掌握
3-5 个面试必知要点。格式固定：
- ✅ **关键词**：一句话解释（不超过 20 字）
- ✅ **关键词**：一句话解释
- ...

#### 💡 核心原理
2-3 段精炼讲解。每段不超过 3 句。格式：
- 第一段：是什么 / 怎么工作
- 第二段：为什么这样设计 / 解决了什么问题
- 第三段（可选）：和相关技术的对比

#### ⚠️ 常见误区
2-3 个对比项。格式固定：
- ❌ 错误理解：xxx
- ✅ 正确理解：xxx

**【可选模块】以下模块根据知识点特性决定是否输出：**

#### 💻 代码示例（有代码的知识点才输出）
不超过 15 行的代码片段 + 1-2 句说明。

#### 🔍 关键细节（面试高频追问点多时输出）
用列表列出 3-5 个面试官常追问的细节。

#### 📊 对比表格（有可比较对象时输出）
如 RDB vs AOF、synchronized vs ReentrantLock 等，用 Markdown 表格。

### 风格规范
1. 标题用 `###` + emoji，全文不用 `#` 和 `##`
2. 要点用 `✅` / `❌` / `**加粗**` 标注重点
3. 每段不超过 3 句话，拒绝大段文字
4. 总长度 500-800 字
5. 不要输出"总结"、"小结"等收尾模块
6. 不要自创新模块

直接输出 Markdown 内容，不要包裹在 JSON 或代码块中。"""


# 高频面试题生成（含 rubric 评分关键点）
LEARN_QUESTIONS_PROMPT = """你是一位资深技术面试辅导专家。请为知识点「{knowledge_point}」生成 3-5 道高频面试题。

## 要求
1. 题目简洁（≤25字），面试官口吻
2. 每道题生成 3-5 个 rubric 评分关键点，**分值之和必须等于 100**
3. 关键点是面试官期望候选人提到的核心要点（≤15字）
4. 参考答案分点列出，和 rubric 关键点对应
5. 覆盖不同考察角度

## 输出格式
```json
{{
  "questions": [
    {{
      "question": "面试题",
      "answer": "1. 要点一\\n2. 要点二\\n3. 要点三",
      "rubric": [
        {{"key_point": "关键点1", "score": 30}},
        {{"key_point": "关键点2", "score": 30}},
        {{"key_point": "关键点3", "score": 40}}
      ]
    }}
  ]
}}
```

只返回 JSON。"""


# 探索对话（带知识上下文）
LEARN_CHAT_PROMPT = """你是一位资深技术面试辅导专家，正在和用户讨论「{knowledge_point}」这个知识点。

## 当前知识点的讲解内容
{content}

## 对话历史
{chat_history}

## 用户当前提问
{user_input}

## 要求
1. 基于上面的知识内容回答用户问题
2. 如果用户问的超出当前知识点范围，简要回答并引导回来
3. 语言简洁专业，面试导向
4. 如果用户引用了知识文本的某个片段，针对该片段进行深入讲解
5. 回答用 Markdown 格式，风格与讲解文章保持一致：
   - 关键词用 `**加粗**`
   - 要点用 ✅/❌ 标注
   - 分点用列表
   - 代码用 ` ``` ` 包裹
   - 每段不超过 3 句

直接输出 Markdown 回答，不要包裹在 JSON 中。"""


# 对话补充到讲解（用户确认后）
LEARN_MERGE_PROMPT = """请将以下对话中的有价值内容，合理融入到原有的知识讲解文章中。

## 原有讲解
{original_content}

## 对话内容（需要融入的）
{chat_content}

## 合并规则
1. **保持模块结构**：原文的模块（📌 一句话概述 / 🔑 必须掌握 / 💡 核心原理 / ⚠️ 常见误区 等）不能删改或重新排序
2. **插入到对应模块**：新内容插入到语义最匹配的已有模块下
3. **风格一致**：新内容要用和原文相同的格式（**加粗**、✅/❌、列表、代码块等）
4. **不删不重**：不删除原有内容，不重复已有内容
5. **自然融入**：不要标注“新增”“补充”等字样
6. 如果对话内容不属于任何已有模块，可以追加到 🔍 关键细节 模块

直接输出合并后的完整 Markdown，不要包裹在 JSON 或代码块中。"""
